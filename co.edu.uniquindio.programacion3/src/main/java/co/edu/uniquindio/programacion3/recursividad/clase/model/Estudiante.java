package co.edu.uniquindio.programacion3.recursividad.clase.model;

public class Estudiante
{
	private int edad;
	private String nombre;

	public Estudiante(){
		this.edad=20;

	}

	public int getEdad() {
		return edad;
	}

	public void setEdad(int edad) {
		this.edad = edad;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
}
